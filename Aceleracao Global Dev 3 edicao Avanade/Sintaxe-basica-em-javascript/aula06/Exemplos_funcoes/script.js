//tipos de função
//declarativas
function funcao() {
    console.log("Esta é uma função declarativa!")
}
funcao();

// expressão de função
var funcao = function /*funcao*/() {
    console.log("Esta é uma função expressão!")
}
funcao();

// arrow function
var funcao = () => {
    console.log("Esta é uma arrow function!");
}
funcao();